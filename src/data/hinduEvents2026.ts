// Get events for a specific date
export function getEventsForDate(date: string): CalendarEvent[] {
  return hinduEvents2026.filter(event => event.date === date);
}

// Get events for a specific month (0-11)
export function getEventsForMonth(month: number, year: number = 2026): CalendarEvent[] {
  const monthStr = String(month + 1).padStart(2, '0');
  const yearStr = String(year);
  return hinduEvents2026.filter(event => event.date.startsWith(`${yearStr}-${monthStr}`));
}

// Check if a date has events
export function hasEvents(date: string): { hasVrat: boolean; hasUtsav: boolean } {
  const events = getEventsForDate(date);
  return {
    hasVrat: events.some(e => e.type === 'vrat'),
    hasUtsav: events.some(e => e.type === 'utsav'),
  };
}
